Детский оздоровительный лагерь «Кубань» — это современный комплекс в одном из лучших уголков на берегу Черного моря — курортном поселке Новомихайловский неподалеку от Туапсе. Поселок окружён горными грядами Кавказского хребта, поросшего лесом. Природный ландшафт, чистый горный воздух и мягкий климат делают это место уникальным для оздоровления детей.

Каждую летнюю смену «Кубань» одновременно принимает до 700 ребят со всей России, многие из которых стараются обязательно приехать снова на следующий год.

Популярность лагерю создают не только природные условия. «Кубань» оборудована множеством спортивных площадок, тренировочных залов, имеет обширную инфраструктуру развлечений. Дети здесь проживают в комфортных условиях, питаются только здоровыми продуктами кубанских производителей.

Воспитатели, вожатые, тренеры и специалисты творческих профессий создают в лагере атмосферу сплоченной команды. Всю смену дети не только отдыхают и развлекаются. В увлекательной форме они приобщаются к искусству, наукам и спортивному образу жизни.
